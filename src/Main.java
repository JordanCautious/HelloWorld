public class Main {

    public static void main(String[] args) {
        // Integers
        int x = 1;
        x += 2;
        System.out.println(x);

        // Strings
        String message = "Hello";
        message += " World!";
        System.out.println(message);

        // Practice with Casting
        double x1 = 1.1;
        double y1 = x1+2;
        System.out.println(y1);
        double x2 = 1.2;
        int y2 = (int)x2 + 2;
        System.out.println(y2);
    }
}